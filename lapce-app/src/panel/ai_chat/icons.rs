/// Helper function to create an icon view with standard sizing
/// Size should be 12.0 for most icons, 14.0 for mic
pub fn icon_view(icon_svg: &'static str, size: f64) -> impl floem::View {
    use floem::views::{Decorators, svg};
    
    svg(move || icon_svg.to_string())
        .style(move |s| s.width(size).height(size))
}
